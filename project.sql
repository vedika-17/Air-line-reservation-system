CREATE DATABASE airline_db;
USE airline_db;

CREATE TABLE AIRPORT (
    Airport_id VARCHAR(10) PRIMARY KEY,
    Airport_name VARCHAR(100) NOT NULL,
    City VARCHAR(100) NOT NULL,
    Country VARCHAR(100) NOT NULL
);

CREATE TABLE FLIGHTS (
    Flight_id INT PRIMARY KEY AUTO_INCREMENT,
    Airline VARCHAR(100) NOT NULL,
    Origin VARCHAR(10) NOT NULL,
    Destination VARCHAR(10) NOT NULL,
    Departure_time TIME NOT NULL,
    Arrival_time TIME NOT NULL,
    Total_Seats INT NOT NULL,
    Price DECIMAL(10,2) NOT NULL,
    FOREIGN KEY (Origin) REFERENCES AIRPORT(Airport_id) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Destination) REFERENCES AIRPORT(Airport_id) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE FLIGHT_AVAILABILITY (
    Flight_id INT NOT NULL,
    Journey_Date DATE NOT NULL,
    Available_Seats INT NOT NULL,
    PRIMARY KEY (Flight_id, Journey_Date),
    FOREIGN KEY (Flight_id) REFERENCES FLIGHTS(Flight_id) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT chk_avail_seats CHECK (Available_Seats >= 0)
);

CREATE TABLE PASSENGERS (
    Passenger_id INT PRIMARY KEY AUTO_INCREMENT,
    Name VARCHAR(100) NOT NULL,
    Email_id VARCHAR(100) NOT NULL,
    Phone_num VARCHAR(15) NOT NULL
);

CREATE TABLE RESERVATION (
    PNR VARCHAR(20) NOT NULL,
    Passenger_ID INT NOT NULL,
    Flight_ID INT NOT NULL,
    Seat_No VARCHAR(10) NOT NULL,
    Booking_Date DATE NOT NULL,
    Journey_Date DATE NOT NULL, 
    PRIMARY KEY (PNR, Passenger_ID),  
    UNIQUE (Flight_ID, Seat_No, Journey_Date),      
    FOREIGN KEY (Passenger_ID) REFERENCES PASSENGERS(Passenger_id) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (Flight_ID) REFERENCES FLIGHTS(Flight_id) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE PAYMENT (
    Payment_ID INT PRIMARY KEY AUTO_INCREMENT,
    PNR VARCHAR(20) NOT NULL,
    Amount DECIMAL(10,2) NOT NULL,
    Payment_method VARCHAR(50) NOT NULL,
    Transaction_date DATETIME NOT NULL,
    UNIQUE (PNR),  
    FOREIGN KEY (PNR) REFERENCES RESERVATION(PNR) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE BAGGAGE (
    Baggage_id INT PRIMARY KEY AUTO_INCREMENT,
    Passenger_ID INT NOT NULL,
    PNR VARCHAR(20) NOT NULL,
    Weight DECIMAL(5,2) NOT NULL,
    Baggage_type VARCHAR(50) NOT NULL,
    FOREIGN KEY (Passenger_ID, PNR) REFERENCES RESERVATION(Passenger_ID, PNR) ON DELETE CASCADE ON UPDATE CASCADE
);

